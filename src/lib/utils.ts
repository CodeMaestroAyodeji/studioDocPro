import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export const formatCurrency = (amount: number) => {
  return new Intl.NumberFormat("en-NG", {
    style: "currency",
    currency: "NGN",
  }).format(amount);
};

export function hasDashboardAccess(role: string, section: string) {
  const permissions = {
    admin: ["*"],
    accountant: ["finance", "tax"],
    project_manager: ["projects", "vendors", "clients"],
    viewer: ["summary"],
  };
  return permissions[role]?.includes(section) || permissions[role]?.includes("*");
}

